    private void BotaoEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BotaoEditarMouseClicked
        try {
            String nome = jTextField1.getText();
            String quantidadeStr = jTextField2.getText();

            if (nome.isEmpty() || quantidadeStr.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Todos os campos devem ser preenchidos.");
                return;
            }

            int quantidade;
            try {
                quantidade = Integer.parseInt(quantidadeStr);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Quantidade deve ser um número válido.");
                return;
            }

            itemAtual.setNome(nome);
            itemAtual.setVolume(quantidade);

            service.atualizar(itemAtual);
            JOptionPane.showMessageDialog(this, "Item atualizado com sucesso.");
            dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao atualizar item: " + e.getMessage());
        }
    }//GEN-LAST:event_BotaoEditarMouseClicked

