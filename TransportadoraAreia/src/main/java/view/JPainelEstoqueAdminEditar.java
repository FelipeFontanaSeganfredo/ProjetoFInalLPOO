    private void carregarEstoque() {
        try {
            itemAtual = service.buscarPorId(itemId.longValue());
            if (itemAtual != null) {
                jTextField1.setText(itemAtual.getNome());
                jTextField2.setText(String.valueOf(itemAtual.getVolume()));
            } else {
                JOptionPane.showMessageDialog(this, "Item não encontrado.");
                dispose();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar item: " + e.getMessage());
            dispose();
        }
    }
